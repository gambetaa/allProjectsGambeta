package br.com.fintech.system.model;

public class Endereco {

	private String nmRua;
	private String nrCasa;
	private String nrCep;
	private String dsComplemento;
	private String dsReferencia;
	//private Bairro bairro;
	private String nmBairro;
	private String nmCidade;
	private String nmEstado;
	public String getNmRua() {
		return nmRua;
	}
	public void setNmRua(String nmRua) {
		this.nmRua = nmRua;
	}
	public String getNrCasa() {
		return nrCasa;
	}
	public void setNrCasa(String nrCasa) {
		this.nrCasa = nrCasa;
	}
	public String getNrCep() {
		return nrCep;
	}
	public void setNrCep(String nrCep) {
		this.nrCep = nrCep;
	}
	public String getDsComplemento() {
		return dsComplemento;
	}
	public void setDsComplemento(String dsComplemento) {
		this.dsComplemento = dsComplemento;
	}
	public String getDsReferencia() {
		return dsReferencia;
	}
	public void setDsReferencia(String dsReferencia) {
		this.dsReferencia = dsReferencia;
	}
	public String getNmBairro() {
		return nmBairro;
	}
	public void setNmBairro(String nmBairro) {
		this.nmBairro = nmBairro;
	}
	public String getNmCidade() {
		return nmCidade;
	}
	public void setNmCidade(String nmCidade) {
		this.nmCidade = nmCidade;
	}
	public String getNmEstado() {
		return nmEstado;
	}
	public void setNmEstado(String nmEstado) {
		this.nmEstado = nmEstado;
	}

}
